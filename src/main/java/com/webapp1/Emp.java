package com.webapp1;

public class Emp {
	
	private int eid;
	private String ename;
	private String elang;
	
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getElang() {
		return elang;
	}
	public void setElang(String elang) {
		this.elang = elang;
	}
	
	

}
